def get_bert_encoder(text_input, bert_model_name):

  if bert_model_name == 'bert_prt_small':
    import json
    from official.nlp.bert import configs, bert_models
    gs_folder_bert = '../trabalho_final/bert-base-portuguese-cased_tensorflow_checkpoint'

    bert_config_file = os.path.join(gs_folder_bert, "bert_config.json")
    config_dict = json.loads(tf.io.gfile.GFile(bert_config_file).read())

    bert_config = configs.BertConfig.from_dict(config_dict)

    _, bert_encoder = bert_models.classifier_model(
        bert_config, num_labels=2)

    preproc_bert_model_name = 'small_bert/bert_en_uncased_L-12_H-512_A-8'
    tfhub_handle_preprocess = map_model_to_preprocess[preproc_bert_model_name]
    preprocessing_layer = hub.KerasLayer(tfhub_handle_preprocess, name='preprocessing')
    encoder_inputs = preprocessing_layer(text_input)

    outputs = bert_encoder(encoder_inputs)[1]

  elif bert_model_name == 'bert_prt_large':
 
    import json
    from official.nlp.bert import configs, bert_models
    gs_folder_bert = '../trabalho_final/bert-large-portuguese-cased_tensorflow_checkpoint'

    bert_config_file = os.path.join(gs_folder_bert, "bert_config.json")
    config_dict = json.loads(tf.io.gfile.GFile(bert_config_file).read())

    bert_config = configs.BertConfig.from_dict(config_dict)

    _, bert_encoder = bert_models.classifier_model(
        bert_config, num_labels=2)

    preproc_bert_model_name = 'small_bert/bert_en_uncased_L-12_H-512_A-8'
    tfhub_handle_preprocess = map_model_to_preprocess[preproc_bert_model_name]
    preprocessing_layer = hub.KerasLayer(tfhub_handle_preprocess, name='preprocessing')
    encoder_inputs = preprocessing_layer(text_input)

    outputs = bert_encoder(encoder_inputs)[1]

  else: 
    tfhub_handle_encoder = map_name_to_handle[bert_model_name]
    tfhub_handle_preprocess = map_model_to_preprocess[bert_model_name]  
    
    preprocessing_layer = hub.KerasLayer(tfhub_handle_preprocess, name='preprocessing')
    encoder_inputs = preprocessing_layer(text_input)
    encoder = hub.KerasLayer(tfhub_handle_encoder, trainable=True, name='BERT_encoder')
    outputs = encoder(encoder_inputs)['pooled_output']

  return outputs

def get_default_bert_mlp(encoder_out):
  net = encoder_out
  net = tf.keras.layers.Dropout(0.1)(net)
  net = tf.keras.layers.Dense(1, activation='sigmoid', name='classifier')(net)
  return net

def get_conv_1_bert_mlp(encoder_out):
  net = encoder_out
  net = tf.keras.layers.Reshape((1, 32,16))(net)
  net = tf.keras.layers.Conv1D(filters=128, kernel_size=5, padding='same', activation='relu',
                         input_shape=(net.shape[1], 1))(net)
  net = tf.keras.layers.Flatten()(net)
  net = tf.keras.layers.Dropout(0.1)(net)
  net = tf.keras.layers.Dense(1, activation='sigmoid', name='classifier')(net)
  return net

def get_conv_1_dense_2_bert_mlp(encoder_out):
  net = encoder_out
  net = tf.keras.layers.Reshape((1, 32,32))(net)
  net = tf.keras.layers.Conv1D(filters=128, kernel_size=5, padding='same', activation='relu',
                         input_shape=(net.shape[1], 1))(net)
  net = tf.keras.layers.Flatten()(net)
  net = tf.keras.layers.Dense(128, activation='sigmoid')(net)
  net = tf.keras.layers.Dropout(0.1)(net)
  net = tf.keras.layers.Dense(1, activation='sigmoid', name='classifier')(net)
  return net


def build_bert_model(bert_model_name, mlp_model_name):
    text_input = tf.keras.layers.Input(shape=(), dtype=tf.string, name='text')
    encoder_out = get_bert_encoder(text_input, bert_model_name)
    
    mlp = eval('get_'+mlp_model_name)
    
    mlp_out = mlp(encoder_out)
    return tf.keras.Model(text_input, mlp_out)

